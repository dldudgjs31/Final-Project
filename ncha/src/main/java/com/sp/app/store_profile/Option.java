package com.sp.app.store_profile;

public class Option {
	private String optionDetail;
	private int option_stock;
	private int optionNum;
	private int productNum;
	public String getOptionDetail() {
		return optionDetail;
	}
	public void setOptionDetail(String optionDetail) {
		this.optionDetail = optionDetail;
	}

	public int getOption_stock() {
		return option_stock;
	}
	public void setOption_stock(int option_stock) {
		this.option_stock = option_stock;
	}
	public int getOptionNum() {
		return optionNum;
	}
	public void setOptionNum(int optionNum) {
		this.optionNum = optionNum;
	}
	public int getProductNum() {
		return productNum;
	}
	public void setProductNum(int productNum) {
		this.productNum = productNum;
	}
	
}
